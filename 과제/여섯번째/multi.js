let number1 = 6;
console.log(number1);
if (number1 % 2 === 0 && number1 % 3 === 0) {
  console.log('6의 배수');
} else if (number1 % 2 === 0 && !number1 % 3 === 0) {
  console.log('2의 배수');
} else if (number1 % 3 === 0 && !number1 % 2 === 0) {
  console.log('3의 배수');
} else if (!number1 % 3 === 0 && !number1 % 2 === 0) {
  console.log('2의 배수도 아니고, 3의 배수도 아니다');
}
